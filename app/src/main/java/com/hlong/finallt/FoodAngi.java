package com.hlong.finallt;

public class FoodAngi {
    private int img;
    private String tittle;

    public FoodAngi(int img, String tittle) {
        this.img = img;
        this.tittle = tittle;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getTittle() {
        return tittle;
    }

    public void setTittle(String tittle) {
        this.tittle = tittle;
    }
}
